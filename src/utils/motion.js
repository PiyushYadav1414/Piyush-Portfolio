// Exporting the `staggerContainer` function to create animation configurations for Framer Motion.
// This function helps in staggering (delaying) animations of child elements within a container.
export const staggerContainer = (staggerChildren, delayChildren) => {
  return {
    // `hidden`: Defines the initial state of the animation for the container.
    hidden: {},

    // `show`: Defines the animation state when the container is visible.
    show: {
      // Configures the transition for child elements inside the container.
      transition: {
        // `staggerChildren`: Time (in seconds) between the animations of each child element.
        staggerChildren: staggerChildren, // e.g., 0.2 means each child starts animating 0.2 seconds after the previous one.

        // `delayChildren`: Optional delay (in seconds) before starting the animations of all child elements.
        delayChildren: delayChildren || 0, // Default is 0 if no value is provided.
      },
    },
  };
};

